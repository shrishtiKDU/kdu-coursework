package shrishti.example;

public class student {
    private int id;
    private String name;
    private int age;
    private char grade;



    public int getId(){
        return id;
    }

    public String getName(){
        return name;
    }

    public int getAge(){
        return age;
    }

    public char getGrade(){
        return grade;
    }

    public void setId(int id){
        this.id=id;
    }

    public void setName(String name){
        this.name=name;
    }

    public void setAge(int age){
        this.age=age;
    }

    public void setGrade(char grade){
        this.grade=grade;
    }




}
